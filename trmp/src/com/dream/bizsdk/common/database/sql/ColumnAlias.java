public class ColumnAlias {
    private String column;

    private String alias;

    public ColumnAlias() {

    }

    public ColumnAlias(String table, String alias) {
        this.column = table;
        this.alias = alias;
    }

    public String getColumn() {
        return this.column;
    }

    public void setColumn(String table) {
        this.column = table;
    }

    public String getAlias() {
        return this.alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }
}
